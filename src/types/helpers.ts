export function katakanaToHiraganaText(text: string): string {
	return text
		.split('')
		.map((char) => katakanaToHiragana[char] || char)
		.join('');
}

export function generateRandomHiragana(length: number): string {
	let result = '';
	for (let i = 0; i < length; i++) {
		const randomIndex = Math.floor(Math.random() * allHiragana.length);
		result += allHiragana[randomIndex];
	}
	return result;
}
